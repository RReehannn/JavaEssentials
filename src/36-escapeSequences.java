class escapeSequences {
    public static void main(String[] args){
        System.out.println(" Md \tRehan shakeel");
        System.out.println(" Md \bRehan shakeel");
        System.out.println(" Md \'Rehan\' shakeel");
        System.out.println(" Md \"Rehan\" Shakeel");
        System.out.println(" Md \\Rehan\\ shakeel");
        System.out.println(" Md \nRehan shakeel");

    }
}
